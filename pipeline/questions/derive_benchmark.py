"""Derive pipeline/benchmarks/MA_SNAP_benchmark.csv from question_spec.yaml.

Implements V2 §6.2: for each entry in question_spec.yaml, resolve the
`field_path` against the joined (state_law, agency_program) playbook, render
the question template with {state}/{program} substitutions, stringify the
resolved value as `ground_truth`, pick a representative evidence row, and
emit a row in the V2 §6.2 column order.

The single source of truth is question_spec.yaml + the populated playbooks.
The benchmark CSV is regenerated by this script; do not edit it by hand.

Exits non-zero on resolution failure for any entry. Designed to be run from
the repo root.
"""

from __future__ import annotations

import csv
import json
import re
import sys
from pathlib import Path

import yaml

REPO_ROOT = Path(__file__).resolve().parents[2]
SPEC = REPO_ROOT / "pipeline" / "questions" / "question_spec.yaml"
STATE_LAW = REPO_ROOT / "pipeline" / "playbook" / "state_law" / "MA.json"
AGENCY_PROGRAM = REPO_ROOT / "pipeline" / "playbook" / "agency_program" / "MA_SNAP.json"
BENCHMARK = REPO_ROOT / "pipeline" / "benchmarks" / "MA_SNAP_benchmark.csv"

LIST_INDEX_RE = re.compile(r"^([^\[]+)\[(\d+)\]$")

# Static facts about the MA × SNAP pilot. When the pipeline generalizes
# beyond a single cell these come from a per-state metadata file.
STATE_CODE = "MA"
STATE_FULL = "Massachusetts"
PROGRAM = "SNAP"

# Composition rules for fields whose playbook representation differs from
# the answer the benchmark should test. Each handler receives the resolved
# raw value plus the joined playbook and returns the composed ground_truth
# string.

COMPOSED_PATHS = {
    "state_law.public_records_law.extension.agency_initial_days":
        lambda v, j: f"{v} business days",
    "state_law.public_records_law.extension.supervisor_extension_days":
        lambda v, j: f"{v} business days",
    "state_law.public_records_law.appeal_path.deadline_days":
        lambda v, j: f"{v} {j['state_law']['public_records_law']['appeal_path']['deadline_unit']} days",
    "agency_program.contact.address_line1":
        lambda v, j: f"{v}, {j['agency_program']['contact']['city']}, {j['agency_program']['contact']['state']}",
}


CSV_COLUMNS = [
    "field_path",
    "question_template",
    "program",
    "state",
    "ground_truth",
    "source_type",
    "source_ref",
    "quote",
    "retrieved_at",
    "received_at",
    "page",
    "document_title",
    "production_package_id",
    "answer_format",
    "extraction_mode",
]


def load_joined() -> dict:
    state_law = json.loads(STATE_LAW.read_text())
    agency_program = json.loads(AGENCY_PROGRAM.read_text())
    return {"state_law": state_law, "agency_program": agency_program}


def resolve(joined: dict, path: str):
    """Resolve a dotted path against the joined playbook.

    Mirrors the resolver in pipeline/validation/closure_check.py. Returns
    the sentinel `_MISSING` when the path doesn't exist.
    """
    node = joined
    for raw in path.split("."):
        m = LIST_INDEX_RE.match(raw)
        if m:
            key, idx = m.group(1), int(m.group(2))
            if not isinstance(node, dict) or key not in node:
                return "_MISSING"
            node = node[key]
            if not isinstance(node, list) or idx >= len(node):
                return "_MISSING"
            node = node[idx]
        else:
            if not isinstance(node, dict) or raw not in node:
                return "_MISSING"
            node = node[raw]
    return node


def stringify(value, answer_format: str) -> str:
    """Stringify a resolved playbook value for the benchmark ground_truth.

    Booleans -> 'yes'/'no'. Lists -> '; '-joined. Integers -> str(int).
    None -> '' (only valid for not_available entries).
    """
    if value is None:
        return ""
    if isinstance(value, bool):
        return "yes" if value else "no"
    if isinstance(value, list):
        return "; ".join(str(v) for v in value)
    if isinstance(value, (int, float)):
        return str(value)
    return str(value)


def parse_index_suffix(field_path: str) -> tuple[str, int | None]:
    """If field_path contains '[N]', return (section_prefix, index).

    Otherwise return (None, None). Used to navigate evidence inside
    benefits_tech_systems[N].
    """
    m = re.search(r"^(agency_program\.benefits_tech_systems)\[(\d+)\]\.(.+)$", field_path)
    if m:
        return m.group(3), int(m.group(2))
    return None, None


def pick_evidence(joined: dict, field_path: str) -> dict | None:
    """Pick the representative evidence row for a field path.

    Strategy:
      1. Determine the section: state_law, agency_program (top-level), or
         agency_program.benefits_tech_systems[N].
      2. Strip the section prefix from field_path to get the search key.
      3. Look for an exact match in that section's evidence[]. If none,
         look for any evidence row whose field is a prefix of the search
         key or vice versa. If still none, return the first evidence row
         in that section. If the section has no evidence, return None.
    """
    sub_path, idx = parse_index_suffix(field_path)
    if sub_path is not None:
        system = joined["agency_program"]["benefits_tech_systems"][idx]
        evidence_rows = system.get("evidence", [])
        search_key = sub_path
    elif field_path.startswith("state_law."):
        evidence_rows = joined["state_law"]["evidence"]
        search_key = field_path[len("state_law."):]
    elif field_path.startswith("agency_program."):
        evidence_rows = joined["agency_program"]["evidence"]
        search_key = field_path[len("agency_program."):]
    else:
        return None

    for row in evidence_rows:
        if row.get("field") == search_key:
            return row
    for row in evidence_rows:
        f = row.get("field", "")
        if f and (search_key.startswith(f) or f.startswith(search_key)):
            return row
    if "." in search_key:
        parent = search_key.rsplit(".", 1)[0]
        for row in evidence_rows:
            f = row.get("field", "")
            if f.startswith(parent + ".") or f == parent:
                return row
    return evidence_rows[0] if evidence_rows else None


def render_template(template: str) -> str:
    return template.replace("{state}", STATE_FULL).replace("{program}", PROGRAM)


def main() -> int:
    spec = yaml.safe_load(SPEC.read_text())
    joined = load_joined()

    rows: list[dict] = []
    failures: list[tuple[str, str]] = []

    for entry in spec:
        fp = entry["field_path"]
        val = resolve(joined, fp)
        if val == "_MISSING":
            failures.append((fp, "field_path does not resolve against joined playbook"))
            continue

        if fp in COMPOSED_PATHS and val is not None:
            ground_truth = COMPOSED_PATHS[fp](val, joined)
        else:
            ground_truth = stringify(val, entry["answer_format"])

        if entry["answer_format"] != "not_available" and ground_truth == "":
            failures.append(
                (fp, "ground_truth is empty but answer_format is not 'not_available'")
            )
            continue

        ev = pick_evidence(joined, fp)
        if ev is None:
            failures.append((fp, "no evidence row available for the section"))
            continue

        row = {
            "field_path": fp,
            "question_template": render_template(entry["question_template"]),
            "program": PROGRAM,
            "state": STATE_CODE,
            "ground_truth": ground_truth,
            "source_type": ev.get("source_type", ""),
            "source_ref": ev.get("source_ref", ""),
            "quote": ev.get("quote", ""),
            "retrieved_at": ev.get("retrieved_at", "") or "",
            "received_at": ev.get("received_at", "") or "",
            "page": ev.get("page", "") if ev.get("page") is not None else "",
            "document_title": ev.get("document_title", "") or "",
            "production_package_id": ev.get("production_package_id", "") or "",
            "answer_format": entry["answer_format"],
            "extraction_mode": entry["extraction_mode"],
        }
        rows.append(row)

    print("=" * 72)
    print("Phase 2 derivation  (V2 §6.2)")
    print("=" * 72)
    print(f"Spec:     {SPEC.relative_to(REPO_ROOT)}")
    print(f"Playbook: {STATE_LAW.relative_to(REPO_ROOT)},")
    print(f"          {AGENCY_PROGRAM.relative_to(REPO_ROOT)}")
    print(f"Output:   {BENCHMARK.relative_to(REPO_ROOT)}")
    print()
    print(f"Spec entries: {len(spec)}")
    print(f"Benchmark rows derived: {len(rows)}")
    print(f"Failures: {len(failures)}")

    if failures:
        print()
        print("--- DERIVATION FAILURES ---")
        for fp, reason in failures:
            print(f"  FAIL {fp:70} {reason}")
        print()
        print("FAIL  Derivation aborted; benchmark not written.")
        return 1

    BENCHMARK.parent.mkdir(parents=True, exist_ok=True)
    with BENCHMARK.open("w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=CSV_COLUMNS, lineterminator="\n")
        writer.writeheader()
        for row in rows:
            writer.writerow(row)

    print()
    print(f"PASS  Wrote {len(rows)} rows to {BENCHMARK.relative_to(REPO_ROOT)}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
